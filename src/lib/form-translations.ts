// Helper to get a translated string, always falling back to English
export function t(lang: TransLang, key: string): string {
  return FORM_T[lang]?.[key] || FORM_T.en[key] || key;
}

// Helper to show bilingual label: "English Label" or "English Label / নেটিভ লেবেল"
export function biLabel(lang: TransLang, key: string): string {
  const en = FORM_T.en[key] || key;
  if (lang === "en") return en;
  const native = FORM_T[lang]?.[key];
  if (!native || native === en) return en;
  return `${en} / ${native}`;
}

// Helper for description: show native if available, else English
export function biDesc(lang: TransLang, key: string): string {
  const en = FORM_T.en[key] || "";
  if (lang === "en") return en;
  const native = FORM_T[lang]?.[key];
  if (!native || native === en) return en;
  return native;
}
